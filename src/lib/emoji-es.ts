// Traducción de consultas de búsqueda de emoji ES → EN.
//
// El buscador de emoji (emoji-mart, vía BlockNote) sólo indexa palabras clave en
// inglés, así que escribir «fuego» o «corazón» no devuelve nada. Aquí mapeamos
// los términos más habituales en español a su equivalente inglés antes de buscar.
// Las claves se guardan SIN acentos y en minúsculas; la búsqueda normaliza igual.

/** Quita acentos/diacríticos y pasa a minúsculas para comparar de forma laxa. */
function norm(s: string): string {
  return s
    .toLowerCase()
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "");
}

