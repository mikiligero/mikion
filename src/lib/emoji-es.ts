// Claves ordenadas para que la coincidencia por prefijo sea determinista.
const ES_KEYS = Object.keys(ES_EMOJI).sort();

/** Traduce una sola palabra (normalizada) o devuelve `null` si no la conoce. */
function lookupWord(w: string): string | null {
  if (ES_EMOJI[w]) return ES_EMOJI[w];
  // Plural simple: «gatos» → «gato».
  if (w.endsWith("s") && ES_EMOJI[w.slice(0, -1)]) return ES_EMOJI[w.slice(0, -1)];
  // Prefijo: mientras se escribe, «fue» debe resolverse ya a «fire» (de
  // «fuego»); si no, el menú se cierra al no encontrar resultados intermedios
  // en inglés y no se reabre. Tomamos la primera clave que empiece por la
  // palabra.
  if (w.length >= 2) {
    const key = ES_KEYS.find((k) => k.startsWith(w));
    if (key) return ES_EMOJI[key];
  }
  return null;
}

/**
 * Traduce una consulta de búsqueda de emoji del español al inglés palabra por
 * palabra. Las palabras desconocidas se dejan tal cual (así el inglés y los
 * nombres latinos siguen funcionando).
 */
export function translateEmojiQuery(query: string): string {
  const words = query.trim().split(/\s+/).filter(Boolean);
  if (words.length === 0) return query;
  return words.map((raw) => lookupWord(norm(raw)) ?? raw).join(" ");
}
